// Escape user-supplied values before interpolating them into HTML email bodies.
// Prevents stored-XSS / markup injection via fields like bid names or customer names.
export function escapeHtml(value: unknown): string {
  return String(value ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
